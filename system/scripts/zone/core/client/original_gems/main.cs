public class OriginalGemsClientScript : ClientScript
{
	protected override void Load()
	{
		this.LoadAllScripts();
	}

	protected override void Ready(Character character)
	{
		// We only send this script if the GemRevamp feature is
		// disabled. If it is, we change ceratain client functions
		// to display skill gems with their original icons and names.
		if (Feature.IsEnabled("SkillGemRevamp"))
			return;

		this.SendAllScripts(character);

		var sb = new StringBuilder();
		foreach (var gem in Gems)
		{
			sb.AppendLine($"M_GEM_OVERRIDE_ITEMS[{gem.Key}] = {{ Name = \"{gem.Value.Name}\", Image = \"{gem.Value.Image}\" }}");

			if (sb.Length > 1900)
			{
				this.SendRawLuaScript(character, sb.ToString());
				sb.Clear();
			}
		}
	}

