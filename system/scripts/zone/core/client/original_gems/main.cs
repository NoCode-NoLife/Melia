//--- Melia Script ----------------------------------------------------------
// Original Gems
//--- Description -----------------------------------------------------------
// Changes the icons and names of skill gems to their original versions.
//--- Known Bugs ------------------------------------------------------------
// There is currently a small issue where gem icons in the inventory
// won't update on the first login, but they will display correctly
// after a relog or map change and whenever new gems are added.
//---------------------------------------------------------------------------

using System.Collections.Generic;
