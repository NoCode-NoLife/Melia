namespace Melia.Shared.Game.Const
{
	public enum HitEffect : byte
	{
		/// <summary>
		/// No special effect.
		/// </summary>
		None = 0,

		/// <summary>
		/// Target flashes white.
		/// </summary>
		Flash = 1,

		/// <summary>
		/// Displays an impact effect.
		/// </summary>
		Impact = 2,

		/// <summary>
		/// Displays a more pronounced impact effect.
		/// </summary>
		ImpactHard = 3,
	}
}
